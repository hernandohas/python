# Listas - Parte 2
frutas = ["Banana", "Maçã", "Uva", "Acerola"]
# len()
print(len(frutas))

# append()
frutas.append("Laranja")
print(frutas)

# insert()
frutas.insert(1, "Pera")
print(frutas)

# extend()
frutas.extend(["Pessego", "Mamão"])
print(frutas)

# remove()
frutas.remove("Banana")
print(frutas)

# pop()
frutas.pop()
print(frutas)
frutas.pop(3)
print(frutas)